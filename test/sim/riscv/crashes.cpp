template <riscv::AddressType address_t> void execute(uint64_t max_mem, bits::span<uint8_t> data) {
  riscv::Machine<address_t> machine{std::string_view{}, {.memory_max = max_mem}};

  for (size_t i = 0; i < 2; i++) {
    machine.memory.evict_execute_segments();
    try {
      // Make available on machine
      machine.cpu.init_execute_area(data.data(), 0x1000, data.size());
      auto &seg = machine.cpu.current_execute_segment();

      // Also, make the instructions readable & executable
      machine.copy_to_guest(0x1000, data.data(), data.size());
      machine.memory.set_page_attr(0x1000, riscv::Page::size(), {.read = true, .write = false, .exec = true});
      machine.cpu.jump(0x1000);
      // let's avoid loops

      riscv::DebugMachine<address_t> debug{machine};

      debug.verbose_instructions = true;
      debug.simulate(5000);
    } catch (std::exception &e) {
      // printf(">>> Exception: %s\n", e.what());
    }
  }
}
